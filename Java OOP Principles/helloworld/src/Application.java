
public class Application {

	public static void main(String[] args) {
		
		// Tutorial 1 - Hello world...
		System.out.println("Hello World!");
		
		// Tutorial 2 - Primative Variables, 
		// int 32 bit value (whole number)
		int myInt = 88;
		System.out.println("myInt = " + myInt);
		
		// short 16 bit value number upto around 32,000
		short myShort = 12;
		System.out.println("myShort = " + myShort);
		
		// long
		long myLong = 1234;
		System.out.println("myLong = " + myLong);
		
		// double for floating points
		double myDouble = 123465;
		System.out.println("myDouble = " + myDouble);
		
		// float, make sure you append and 'f' to make it a float not a long
		float myFloat = 12346.5f;
		System.out.println("myFloat = " + myFloat);
		
		// char, for unicode characters using single quotes
		char myChar = 'y';
		System.out.println("myChar = " + myChar);
		
		// boolean for true or false values
		boolean myBoolean = true;
		System.out.println("myBoolean = " + myBoolean);
		
		// byte for 8 bits of positive or neg values of upto +-127
		byte myByte = -123;
		System.out.println("myByte = " + myByte);
		
		
	}

}
